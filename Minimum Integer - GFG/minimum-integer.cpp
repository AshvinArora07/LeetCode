class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends
class Solution {
  public:
        int minimumInteger(int N, vector<int> A) {
        int mmin = INT_MAX;
        long sum = 0;
        for(int i : A)
            sum += i;
        for(int X : A)
            if((long)X*N >= sum && mmin > X)
                {
                    mmin = X;
                }
        return mmin;
    }
};


